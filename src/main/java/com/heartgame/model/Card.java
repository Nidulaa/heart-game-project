package com.heartgame.model;

public class Card {
    private String suit;
    private String rank;

    // Getters and Setters
    public String getSuit() { return suit; }
    public void setSuit(String suit) { this.suit = suit; }
    public String getRank() { return rank; }
    public void setRank(String rank) { this.rank = rank; }
}